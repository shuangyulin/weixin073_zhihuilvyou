					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数

			goupiaoriqiChange(e) {
				this.ruleForm.goupiaoriqi = e.target.value;
				this.$forceUpdate();
			},



			jingdiantupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.jingdiantupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				this.ruleForm.zongjine = this.zongjine
				if(this.ruleForm.menpiaojiage&&(!this.$validate.isIntNumer(this.ruleForm.menpiaojiage))){
					this.$utils.msg(`门票价格应输入整数`);
					return
				}
				if(this.ruleForm.shuliang&&(!this.$validate.isIntNumer(this.ruleForm.shuliang))){
					this.$utils.msg(`数量应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`jingdiangoupiao`, this.ruleForm);
				}else{
					await this.$api.add(`jingdiangoupiao`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
